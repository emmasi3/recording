/*
=============================================================================
关于基于 DXGI + FFMPEG(h264_nvenc) 进行屏幕录制时画面回退（跳帧幽灵帧）现象的总结
=============================================================================

【现象描述】
在使用 DXGI 抓取桌面帧，并且将其存在固定大小的硬件池 (hwFramePool) 中循环写入，以及 FFmpeg (NVENC) 以 h264_nvenc 压制时。如果鼠标静止，屏幕内容没有改变，录制出的视频中偶发性地会出现例如：02秒连续播了一段时间，突然闪过一帧 01 秒画面，随后又回归 02 秒画面的诡异“时钟回退”怪象。
这个现象你可以看，E:\音视频\record_stream_to_rtsp\recording\data_out\video\pic 这个位置里面的图片，是录制下来的h264视频的所有帧，其中在000213.bmp 为02画面的第一帧，那么往后60张应该也是02画面帧，但是，000226.bmp莫名出现了01帧，也就是出现了回退现象

【问题根源】
产生这种“幽灵回退”现象的核心原因在于：
环形硬件帧池（hwFramePool）的大小，与编码器（h264_nvenc）内部流水线的前瞻分析缓存（lookahead buffer）发生冲突，导致的 "显存复用踩踏 (Buffer Overwrite / Race Condition)".

在未加锁和独立分离显存引用的前提下：
1. 你的 DXGI 通过 m_currentIndex 循环复用固定大小（如: TEXTURE_BUFFER_SIZE=4）的纹理池。
2. 你将刚拿到的纹理直接送给 FFmpeg。
3. 如果 FFmpeg 的编码参数（特别是慢速、高前瞻预设）使它在内部留存了数帧以评估画面。
4. 当 DXGI 的环形索引转完一圈，由于太快，它会无视编码器是否消化完了，而直接盖掉这个索引槽原有的物理显存画面写上新图！
5. 结果就是：编码器按照旧的 PTS 时标去读那片显存去压缩，结果提取却发现画上面的内容已经被你的 DXGI 的最新帧污染了！它误将污染图压为老时间的帧。然后可能在队列中另一帧里幸存了老图，形成了交织输出倒错，表现出了时钟闪烁的撕裂感。

【现象论证与测试对照记录】
针对上述情况我们进行了验证：

(1) 仅修改 NVENC 参数为低延迟、无前瞻配置：
    av_opt_set(m_ctx->priv_data, "delay", "0", 0);
    av_opt_set(m_ctx->priv_data, "preset", "llhq", 0);
    av_opt_set(m_ctx->priv_data, "rc-lookahead", "0", 0);
    av_opt_set(m_ctx->priv_data, "zerolatency", "1", 0);
    并且 TEXTURE_BUFFER_SIZE=4 时，画面完全没有回退。
    分析：强制无延迟的 NVENC，塞进一帧立刻消耗一帧，池子里面的旧数据已经没用了，所以再覆盖复用也不会踩到编码器还在读的缓存。

(2) TEXTURE_BUFFER_SIZE=4，不对编码器做任何 priv_data 私有设置(不加slow等)：
    默认参数画面平稳。
    分析：NVENC 默认是不开启深度的前瞻缓冲（或者非常少的），这就不足以发生环形缓冲追尾。

(3) TEXTURE_BUFFER_SIZE=8，不对编码器做私有配置：
    画面平稳。没有任何交接踩踏。

(4) TEXTURE_BUFFER_SIZE=4，仅设置 preset="llhq"：
    无回退，一切正常。
    分析：llhq 也是偏向低延迟(low latency)的工作模式，积压少。

(5) TEXTURE_BUFFER_SIZE=4，仅设置 preset="slow"：
    问题爆发，产生频繁画面回退和乱序幽灵帧。
    分析：slow 预设极大幅度打开了其内部 rc-lookahead 和深度 B 帧等预读缓存区滞留！它把 4 个槽的帧全部据为己有分析，你的指针瞬间绕弯重盖时，直接修改了它手里还没压好的数据！

(6) TEXTURE_BUFFER_SIZE=8，仅设置 preset="slow"：
    回退频率大幅减少，但依然存在。
    分析：由于你帧池扩大到了 8 张，追尾概率下降了，它需要更久你才能再次写到那个重叠的位置，但只要静止足够久导致的发送激增频率够大，它依然会在偶发情况下碰撞！

【解决方案说明】
通过上述现象分析，想要彻底解决这类问题，可以从两方面入手（推荐组合或根据场景选用1和2之一）：

策略一：（最佳实时体验流媒体策略 - 即我们采用的测试5思路）
直接通知 NVENC 开启它为了直播流媒体打造的低延迟模式，关闭滞纳队列池：
强制设置 zerolatency = 1 以及 delay=0 和 rc-lookahead=0。
此时环形池设置 4 张足以完全避免覆盖，并且大大降低编码产生的硬延迟。适合对实时响应要求极高的环境，不会产生幽灵帧。（缺点是对画面码率动态评估不够完全，质量轻微影响）

策略二：（物理隔离、增大池身）
如果你必须追求极端画质，也就是保留 preset="slow"。那就要将你的 TEXTURE_BUFFER_SIZE(池容量) 无限增大，大到彻底躲开它的留存周期，比如设置到 60！但由于您考虑到了大量分配 D3D11 Texture2D 会无限制占据巨额显存内存并可能产生带宽管理隐患（在某些情况还会出现 E_OUTOFMEMORY 警告），这是有风险和不良后果边界的。
如果一定要这么做，在代码上最好不是环形强塞，而是利用 D3D11 CopyResource 实施 Deep Copy 做内存阻断以让出 GPU 控制权保障。

【总结与收获】
在音视频开发中，要非常敬畏任何硬件帧 (AVHWFramesContext) 和异步执行硬件接口 (比如各种显卡的硬件编码器) 。当进行底层零拷贝内存转手时：只要编码器还没有将引用放弃掉（尚未从底层退出队列），绝对不能盲目去对这个指针对应的那块显存内容进行强制改写，否则将伴随可怕的污染交错。
*/